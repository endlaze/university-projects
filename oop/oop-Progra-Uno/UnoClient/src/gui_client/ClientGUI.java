    public void updateDeckView(){
        jPanel1.removeAll();
        jPanel1.revalidate();
        jPanel1.repaint();
        IRemoteUno handler = UnoClient.getUno();
        ArrayList<String> imageNames = new ArrayList<>();
        try {
            imageNames = handler.getHand(playerName);
        } catch (RemoteException ex) {
            Logger.getLogger(ClientGUI.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        String imageName;
        for(int i=0;i<imageNames.size();i++){
            imageName = imageNames.get(i);
            JButton jb;
            String path = "resources/cards_images/"+imageName;
            
          
            jb = new JButton(new ImageIcon(getClass().getClassLoader().getResource(path)));
            jb.setBorder(new EmptyBorder(1,1,1,1));
            jPanel1.add(jb);
            StringBuilder sb=new StringBuilder(imageName);
            int m=Math.max(0, imageName.length() - 4);
            sb.setLength(m);
     
            final String imageName2=sb.toString();
            final String delImg=imageName;
            
            jb.addActionListener(new ActionListener() {
                @Override
                public void actionPerformed(ActionEvent e) {
                 
                    Boolean valid=false;
                    final String imgName=imageName2;
                   
                    try {
                        valid=UnoClient.getUno().validateLastCard(imgName,playerName);
                    } catch (RemoteException ex) {
                        Logger.getLogger(ClientGUI.class.getName()).log(Level.SEVERE, null, ex);
                    }
                    
                    if (valid) {
                        
                        if(imgName.startsWith("W")){
                            new ColorSelection().setVisible(true);
                        }
                        jLabel1.setIcon(new ImageIcon(getClass().getClassLoader().getResource(path)));
                        jPanel1.remove(jb);
                        jPanel1.revalidate();
                        jPanel1.repaint();
                   
                        
                    }

                }
            });
        }
        jPanel1.updateUI();  
       
        
    }
    
    
    
    
    
    
    
    
