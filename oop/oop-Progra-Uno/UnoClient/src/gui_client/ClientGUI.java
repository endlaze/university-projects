    public void setPlayersInfo(){
        
        jPlayers.removeAll();
        jPlayers.revalidate();
        jPlayers.repaint();
        jPlayers.setLayout(new BoxLayout(jPlayers, BoxLayout.Y_AXIS));
        
        
        
        
        try {
            
            //Players and number of cards
            ArrayList<String>playersInfo=UnoClient.getUno().getPlayersNames();
            ArrayList<String>playersNumberOfCards=UnoClient.getUno().getPlayersNumberOfCards();
            
            ////////////////// Loop to add each player and number of cards
            for(int pi=0;pi<playersInfo.size();pi++){
                 JPanel completeInfo=new JPanel();
                 completeInfo.setLayout(new FlowLayout());
                 completeInfo.setBorder(new LineBorder(Color.BLACK));
                 
                 
                 //Label for player
                JLabel playerLabel=new JLabel();
                playerLabel.setText(playersInfo.get(pi));
                playerLabel.setBorder(new EmptyBorder(0,4,3,2));
                playerLabel.setPreferredSize(new Dimension(200,15));
                 
                 
                 
                 //Label for number
                 JLabel numberLabel=new JLabel();
                 numberLabel.setText(playersNumberOfCards.get(pi));
                 numberLabel.setBorder(new EmptyBorder(5,4,3,2));
                 
                 //-------------------------------------------------
             
                
                

                completeInfo.add(playerLabel);
                completeInfo.add(numberLabel);
                jPlayers.add(completeInfo);
                
                
                
            }
            jPlayers.updateUI();
            
            
            
        } catch (RemoteException ex) {
            Logger.getLogger(ClientGUI.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
        
    }
    
    
    
    

